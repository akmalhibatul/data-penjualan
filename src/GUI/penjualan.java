    private void tpBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tpBTNActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb1 = (DefaultTableModel)table1.getModel();

        String noTransaksi = notTF.getText();
        String tglPesan = tglPPTF.getText();
        String kodeSekolah = ksPTF.getText();
        String namaSekolah = nsPTF.getText();
        int JumlahAkun = Integer.parseInt(jaPTF.getText());
        int TotalBayar = Integer.parseInt(tbPTF.getText());
        
        String kelas = "";
        if(kelasPCB.getSelectedIndex()== 0){
            kelas = "";
        }else if(kelasPCB.getSelectedIndex()== 1){
            kelas = "KELAS 4";
        } else if(kelasPCB.getSelectedIndex()== 2){
            kelas = "KELAS 5";
        } else if(kelasPCB.getSelectedIndex()== 3){
            kelas = "KELAS 6";
        } else if(kelasPCB.getSelectedIndex()== 4){
            kelas = "KELAS 8";
        } else if(kelasPCB.getSelectedIndex()== 5){
            kelas = "KELAS 10";
        }
        String caraBayar = "";
        String bank = "";
        if(danaBos.isSelected()){
            caraBayar = "DANA BOS";
            bank = "DKI DIS";
        }else{
            caraBayar = "DANA BOP";
            bank = "BCA PE";
        }
        
        String statusBayar = "";
        if(bayarCB.isSelected()){
            statusBayar = "LUNAS";
        }else{
            statusBayar = "BELUM BAYAR";
        }
        
        tb1.addRow(new Object[]{
            (noTransaksi), (tglPesan), (namaSekolah), (kelas), (JumlahAkun), (TotalBayar), (caraBayar), (bank), (statusBayar)
        });
        

        //clear
        notTF.setText("");
        tglPPTF.setText("");
        ksPTF.setText("");
        nsPTF.setText("");
        kelasPCB.setSelectedIndex(0);
        jaPTF.setText("");
        tbPTF.setText("");
        bayarCB.setSelected(false);
        buttonGroup1.clearSelection();
    }//GEN-LAST:event_tpBTNActionPerformed

    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        int row = table1.getSelectedRow();
        if(row >= 0){
            notTF.setText(table1.getValueAt(row, 0).toString());
            tglPPTF.setText(table1.getValueAt(row, 1).toString());
            nsPTF.setText(table1.getValueAt(row, 2).toString());
            jaPTF.setText(table1.getValueAt(row, 4).toString());
            tbPTF.setText(table1.getValueAt(row, 5).toString());

            String valueToAddToComboBox = table1.getValueAt(row, 3).toString();
            DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) kelasPCB.getModel();
            model.setSelectedItem(valueToAddToComboBox);

            String radioButtonValue = table1.getValueAt(row, 7).toString();

            // Misalnya, Anda memiliki dua radio button dengan nama radioButtonYa dan radioButtonTidak
            if (radioButtonValue.equals("DKI DIS")) {
                danaBos.setSelected(true);
                danaBop.setSelected(false);
            } else if (radioButtonValue.equals("BCA PE")) {
                danaBos.setSelected(false);
                danaBop.setSelected(true);
            } else {
                // Anda dapat menentukan perilaku default jika data tidak cocok dengan pilihan yang ada
                danaBos.setSelected(false);
                danaBop.setSelected(false);
            }
            
            String checkBoxValue = table1.getValueAt(row, 8).toString();

            // Misalnya, Anda memiliki satu JCheckBox dengan nama checkBox1
            if (checkBoxValue.equals("LUNAS")) {
                bayarCB.setSelected(true);
            } else if (checkBoxValue.equals("â€œBELUM BAYAR")) {
                bayarCB.setSelected(false);
            } else {
                // Anda dapat menentukan perilaku default jika data tidak cocok dengan pilihan yang ada
                bayarCB.setSelected(false);
            }
        }

    }//GEN-LAST:event_table1MouseClicked

